#useful string functions
import string

Uppercase_letters = []
Uppercase_letters += string.ascii_uppercase #adds all Uppercase Characters to the Uppercase_letters array
print(Uppercase_letters)

lowercase_letters = []
lowercase_letters += string.ascii_lowercase #adds all lowercase Characters to the lowercase_letters array
print(lowercase_letters)

digits = []
digits += string.digits #adds all numbers to the digits array
print(digits)

special_characters = []
special_characters += string.punctuation #adds all special Characters to the special_characters array
print(special_characters)



